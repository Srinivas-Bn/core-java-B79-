package section5;

public class ternaryoperator {

	public static void main(String[] args) {
		int x = 10;
		x = (10 == x) ? 0 : 1; /*syntax of ternary operator and if the result is true it executes dot part else it executes semicolon 
		                         part*/
		System.out.println(x);
		
		

	}

}
